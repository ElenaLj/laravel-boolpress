<template>
  <div class="single-post">
      <h2>{{post.title}}</h2>
      <img :src="`/storage/${post.image}`" :alt="post.title">
      <p>{{post.content}}</p>
  </div>
</template>

<script>
export default {
    name: "SinglePost",
    data() {
        return {
            post: {}
        }
    },
    created() {
        axios.get(`/api/posts/${this.$route.params.slug}`)
            .then( (response) => {
                this.post = response.data;
            }).catch( (error) => {
                this.$router.push({name: "page-404"});
            });
    }
}
</script>

